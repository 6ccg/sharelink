interface I18nContextProps {
  lang: Language;
  setLang: (lang: Language) => void;
  t: (key: string, vars?: TranslationVars) => string;
}

const I18nContext = createContext<I18nContextProps | undefined>(undefined);

export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLangState] = useState<Language>(() => {
    const saved = localStorage.getItem('sharelink_lang') as Language;
    if (saved === 'zh' || saved === 'en') return saved;
    // Autodetect browser locale
    const browserLang = navigator.language || '';
    return browserLang.toLowerCase().startsWith('zh') ? 'zh' : 'en';
  });

  const setLang = (newLang: Language) => {
    setLangState(newLang);
    localStorage.setItem('sharelink_lang', newLang);
  };

  const t = (key: string, vars?: TranslationVars): string => {
    const langDict = translations[lang] || translations['en'];
    const val = langDict[key];
    if (val !== undefined) return interpolate(val, vars);
    // Fallback to English dictionary
    const fallbackVal = translations['en'][key];
    return fallbackVal !== undefined ? interpolate(fallbackVal, vars) : key;
  };

  return (
    <I18nContext.Provider value={{ lang, setLang, t }}>
      {children}
    </I18nContext.Provider>
  );
};

export const useTranslation = () => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useTranslation must be used within an I18nProvider');
  }
  return context;
};

function interpolate(value: string, vars?: TranslationVars) {
  if (!vars) return value;
  return value.replace(/\{(\w+)\}/g, (_, name: string) => {
    const replacement = vars[name];
    return replacement === undefined ? `{${name}}` : String(replacement);
  });
}
